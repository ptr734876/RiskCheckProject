def get_or_create(model, defaults=None, **lookup):
    item = db.session.scalar(db.select(model).filter_by(**lookup))
    if item is not None:
        return item, False
    params = dict(lookup)
    params.update(defaults or {})
    item = model(**params)
    db.session.add(item)
    db.session.flush()
    return item, True

